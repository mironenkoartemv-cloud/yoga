async function main() {
  await prisma.$executeRawUnsafe(`
    CREATE TABLE IF NOT EXISTS "legal_profiles" (
      "id" TEXT NOT NULL DEFAULT 'main',
      "brand" TEXT NOT NULL DEFAULT 'Asana',
      "legalName" TEXT NOT NULL,
      "shortName" TEXT NOT NULL,
      "director" TEXT NOT NULL,
      "inn" TEXT NOT NULL,
      "kpp" TEXT NOT NULL,
      "ogrn" TEXT NOT NULL,
      "address" TEXT NOT NULL,
      "registrationDate" TEXT NOT NULL,
      "workHours" TEXT NOT NULL,
      "supportPhone" TEXT NOT NULL,
      "supportEmail" TEXT NOT NULL,
      "serviceTitle" TEXT NOT NULL,
      "serviceDescription" TEXT NOT NULL,
      "serviceCountry" TEXT NOT NULL,
      "serviceCurrency" TEXT NOT NULL,
      "serviceWarranty" TEXT NOT NULL,
      "serviceLifetime" TEXT NOT NULL,
      "serviceSafety" TEXT NOT NULL,
      "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT "legal_profiles_pkey" PRIMARY KEY ("id")
    );
  `);

  await prisma.$executeRawUnsafe(`
    CREATE TABLE IF NOT EXISTS "legal_document_versions" (
      "id" TEXT NOT NULL,
      "type" TEXT NOT NULL,
      "title" TEXT NOT NULL,
      "content" TEXT NOT NULL,
      "effectiveFrom" TIMESTAMP(3) NOT NULL,
      "effectiveTo" TIMESTAMP(3),
      "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT "legal_document_versions_pkey" PRIMARY KEY ("id")
    );
  `);

  await prisma.$executeRawUnsafe(`
    CREATE INDEX IF NOT EXISTS "legal_document_versions_type_effectiveTo_idx"
    ON "legal_document_versions"("type", "effectiveTo");
  `);

  await prisma.$executeRawUnsafe(`
    CREATE INDEX IF NOT EXISTS "legal_document_versions_type_effectiveFrom_idx"
    ON "legal_document_versions"("type", "effectiveFrom");
  `);

  await prisma.legalProfile.upsert({
    where: { id: 'main' },
    create: { id: 'main', ...profile },
    update: {},
  });

  for (const document of documents) {
    const exists = await prisma.legalDocumentVersion.findFirst({
      where: { type: document.type, effectiveTo: null },
    });
    if (!exists) {
      await prisma.legalDocumentVersion.create({
        data: {
          ...document,
          effectiveFrom: new Date(),
        },
      });
    }
  }

  console.log('✅ Legal content tables are ready');
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
